    updated_at  timestamp with time zone NOT NULL DEFAULT now(),
    UNIQUE (tenant_id, key)
);

CREATE INDEX IF NOT EXISTS doppio_inventory_tenant_idx
    ON public.doppio_inventory (tenant_id);

-- Bills / Sales Invoices
CREATE TABLE IF NOT EXISTS public.doppio_bills (
    id              bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id       uuid REFERENCES public.saas_tenants(id) ON DELETE CASCADE,
    "orderId"       text NOT NULL,
