    available   boolean NOT NULL DEFAULT true,
    prep_time   integer DEFAULT 5,
    recipe_specs jsonb DEFAULT '{}'::jsonb,
    created_at  timestamp with time zone NOT NULL DEFAULT now(),
    UNIQUE (tenant_id, name)
);

CREATE INDEX IF NOT EXISTS doppio_menu_tenant_idx
    ON public.doppio_menu (tenant_id);

-- Inventory Stock Levels (key-value, one row per ingredient per tenant)
CREATE TABLE IF NOT EXISTS public.doppio_inventory (
    id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
