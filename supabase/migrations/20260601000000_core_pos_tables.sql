    "shiftId"       text NOT NULL,
    "cashierName"   text NOT NULL DEFAULT '',
    "openingFloat"  numeric NOT NULL DEFAULT 0,
    "closingFloat"  numeric DEFAULT NULL,
    "totalSalesCash" numeric NOT NULL DEFAULT 0,
    "totalSalesUpi"  numeric NOT NULL DEFAULT 0,
    "totalSalesCard" numeric NOT NULL DEFAULT 0,
    "expectedCash"  numeric NOT NULL DEFAULT 0,
    "actualCash"    numeric NOT NULL DEFAULT 0,
