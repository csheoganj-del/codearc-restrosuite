    "customerPhone" text DEFAULT '',
    items           jsonb NOT NULL DEFAULT '[]'::jsonb,
    subtotal        numeric NOT NULL DEFAULT 0,
    gst             numeric NOT NULL DEFAULT 0,
    discount        numeric NOT NULL DEFAULT 0,
    total           numeric NOT NULL DEFAULT 0,
    "paymentMethod" text NOT NULL DEFAULT 'Cash',
    "orderType"     text NOT NULL DEFAULT 'Takeaway',
    "tableNumber"   text DEFAULT '',
    "shiftId"       text DEFAULT '',
    "dateTime"      text NOT NULL DEFAULT '',
    created_at      timestamp with time zone NOT NULL DEFAULT now(),
    UNIQUE (tenant_id, "orderId")
);

CREATE INDEX IF NOT EXISTS idx_doppio_bills_order_id ON public.doppio_bills ("orderId");
CREATE INDEX IF NOT EXISTS idx_doppio_bills_date ON public.doppio_bills ("dateTime");
CREATE INDEX IF NOT EXISTS idx_doppio_bills_tenant_date ON public.doppio_bills (tenant_id, created_at DESC);

