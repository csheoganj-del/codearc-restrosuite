-- Business Profile (one row per tenant)
CREATE TABLE IF NOT EXISTS public.doppio_business_profile (
    id              bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id       uuid REFERENCES public.saas_tenants(id) ON DELETE CASCADE,
    business_name   text NOT NULL DEFAULT 'My Restaurant',
    address         text NOT NULL DEFAULT '',
    phone           text NOT NULL DEFAULT '',
    gst_enabled     boolean NOT NULL DEFAULT false,
    gst_rate        numeric NOT NULL DEFAULT 18.00,
    gst_number      text DEFAULT '',
    upi_vpa         text DEFAULT '',
    loyalty_discount_enabled boolean NOT NULL DEFAULT false,
    loyalty_discount_rate numeric NOT NULL DEFAULT 10,
    passcode_lock_enabled boolean NOT NULL DEFAULT false,
    passcode         text DEFAULT '',
    crm_enabled      boolean NOT NULL DEFAULT true,
    tax_enabled      boolean NOT NULL DEFAULT true,
    sound_enabled    boolean NOT NULL DEFAULT false,
    whatsapp_enabled boolean NOT NULL DEFAULT false,
    whatsapp_gateway_enabled boolean NOT NULL DEFAULT false,
    whatsapp_gateway_url text DEFAULT '',
    whatsapp_gateway_token text DEFAULT '',
    shift_enabled    boolean NOT NULL DEFAULT false,
    shift_default_float numeric NOT NULL DEFAULT 2000,
    shift_max_drawer    numeric NOT NULL DEFAULT 5000,
    shift_pos_lock      boolean NOT NULL DEFAULT true,
    feature_flags   jsonb NOT NULL DEFAULT '{}'::jsonb,
    created_at      timestamp with time zone NOT NULL DEFAULT now(),
    updated_at      timestamp with time zone NOT NULL DEFAULT now(),
    UNIQUE (tenant_id)
);

CREATE INDEX IF NOT EXISTS doppio_business_profile_tenant_idx
    ON public.doppio_business_profile (tenant_id);

-- Menu Items
CREATE TABLE IF NOT EXISTS public.doppio_menu (
    id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id   uuid REFERENCES public.saas_tenants(id) ON DELETE CASCADE,
    name        text NOT NULL,
    description text DEFAULT '',
    price       numeric NOT NULL DEFAULT 0,
    category    text NOT NULL DEFAULT 'Other',
    icon        text DEFAULT '☕',
