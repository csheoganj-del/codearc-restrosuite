-- ============================================================
-- Enable RLS on all core tables
-- (tenant-data edge function uses service_role to bypass RLS)
-- ============================================================
DO $$
DECLARE
    t text;
    core_tables text[] := ARRAY[
        'doppio_business_profile', 'doppio_menu', 'doppio_inventory',
        'doppio_bills', 'doppio_pending_orders', 'doppio_shifts',
        'doppio_shift_events', 'doppio_employees', 'doppio_leave_requests',
        'doppio_attendance', 'doppio_crm', 'doppio_inventory_batches',
        'doppio_notifications', 'doppio_custom_recipes',
        'doppio_inventory_thresholds', 'doppio_pos_popularity',
        'doppio_draft_orders'
    ];
BEGIN
    FOREACH t IN ARRAY core_tables LOOP
        EXECUTE format('ALTER TABLE public.%I ENABLE ROW LEVEL SECURITY', t);
        EXECUTE format('ALTER TABLE public.%I FORCE ROW LEVEL SECURITY', t);
        EXECUTE format('REVOKE ALL ON TABLE public.%I FROM anon, authenticated', t);
    END LOOP;
END $$;
